const months = ['March', 'Jan', 'Feb', 'Dec'];
let result = months.sort();
console.log(result);  // ["Dec", "Feb", "Jan", "March"]
console.log(months);  //[ 'Dec', 'Feb', 'Jan', 'March' ]


